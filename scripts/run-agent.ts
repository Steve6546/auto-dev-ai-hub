import 'dotenv/config';
import { generateProjectBrief, generateTechStackPrompt, generateUIWireframePrompt } from '../src/agents/specificationModule';
import { writeDocumentation } from '../src/agents/docsAgent';
import CodeAgent from '../src/agents/code-agent';
import { callLLM } from '../src/backend/services/llmService';
import storage from 'node-persist';
import { exec } from 'child_process';
import * as fs from 'fs/promises';

/**
 * The main entry point for the AutoDev AI Hub agent.
 * This script orchestrates the different agents and services to perform a round of tasks.
 */
async function main() {
  const roundId = parseInt(process.argv[2], 10);
  if (isNaN(roundId)) {
    console.error('Invalid round ID');
    process.exit(1);
  }

  const config = JSON.parse(await fs.readFile('config.json', 'utf8'));
  const modelId = config.defaultModel;

  let prompt: string;
  let outputPath: string;
  let commitMessage: string;
  let content: string;
  const codeAgent = new CodeAgent();

  if (roundId === 1) {
    console.log(`Generating project brief for round ${roundId}...`);
    prompt = await generateProjectBrief('project_tasks.yaml', roundId);
    outputPath = 'README.md';
    commitMessage = 'docs: Initial README.md generated by Docs Agent for AutoDev AI Hub';
    content = await callLLM(modelId, prompt);
  } else if (roundId === 2) {
    console.log(`Generating tech stack prompt for round ${roundId}...`);
    prompt = await generateTechStackPrompt('project_tasks.yaml', roundId);
    outputPath = 'TECH_STACK.md';
    commitMessage = 'docs: Generated TECH_STACK.md with initial tech stack design for AutoDev AI Hub by Docs Agent';
    content = await callLLM(modelId, prompt);
  } else if (roundId === 3) {
    console.log(`Generating UI wireframe prompt for round ${roundId}...`);
    prompt = await generateUIWireframePrompt('project_tasks.yaml', roundId);
    outputPath = 'ui-wires/dashboard_wireframe.md';
    commitMessage = 'feat: Generated initial UI wireframes for AutoDev AI Hub dashboard by Code/Docs Agents';
    content = await codeAgent.generateCode(prompt);
  } else {
    console.error(`Round ${roundId} is not supported yet.`);
    process.exit(1);
  }

  console.log(`Writing documentation to ${outputPath}...`);
  const dir = outputPath.substring(0, outputPath.lastIndexOf('/'));
  if (dir) {
    await fs.mkdir(dir, { recursive: true });
  }
  await writeDocumentation(outputPath, content);

  console.log('Committing and pushing changes...');
  exec(`git add ${outputPath} && git commit -m "${commitMessage}" && git push`, (error, stdout, stderr) => {
    if (error) {
      console.error(`Error during git operations: ${error}`);
      return;
    }
    console.log(stdout);
    console.error(stderr);
  });
}

main().catch(error => {
  console.error('An unexpected error occurred:', error);
  process.exit(1);
});
